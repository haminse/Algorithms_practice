n = int(input())

go_loc = [None]*n

#make board
board = [[0]*19]*19

# get cordinate
for i in range(n):
    go_loc[i] = list(map(int, input().split()))

for cord in go_loc:
    row, col = cord[0]-1, cord[1]-1
    board[row][col] = 1
